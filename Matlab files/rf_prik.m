sA=0.1700;
L=0.1300;
rp=0.1300;
rb=0.2900;
S=rb*sqrt(3);
a=pi/6;
xc=0.25000;
yc=0.15000;
O1=a+(pi/6);
c1=atan((yc-rp*sin(O1))/(xc-rp*cos(O1)));
R1=sqrt((xc-rp*cos(O1))^2+(yc-rp*sin(O1))^2);
d1=acos((sA^2-L^2+R1^2)/(2*sA*R1));
A1=c1+d1;
O2=a+5*pi/6;
c2=pi+atan((yc-rp*sin(O2))/((xc-rp*cos(O2))-S));
R2=sqrt((S-(xc-rp*cos(O2)))^2+(yc-rp*sin(O2))^2);
d2=acos((sA^2-L^2+R2^2)/(2*sA*R2));
A2=c2+d2;
O3=a+9*pi/6;
c3=pi+atan((-S*sqrt(3)/2+(yc-rp*sin(O3)))/(-S*0.5+(xc-rp*cos(O3))));
R3=sqrt((-S*0.5+(xc-rp*cos(O3)))^2+(-S*sqrt(3)/2+(yc-rp*sin(O3)))^2);
d3=acos((sA^2-L^2+R3^2)/(2*sA*R3));
A3=c3+d3;
disp([A1 A2 A3]*180/pi)
%%
hold on
x11=0;
y11=0;
x21=S;
y21=0;
x31=0.5*S;
y31=S*sqrt(3)/2;
x12=sA*cos(A1);
y12=sA*sin(A1);
x22=x21+sA*cos(A2);
y22=y21+sA*sin(A2);
x32=x31+sA*cos(A3);
y32=y31+sA*sin(A3);
x13=xc-rp*cos(O1);
y13=yc-rp*sin(O1);
x23=xc-rp*cos(O2);
y23=yc-rp*sin(O2);
x33=xc-rp*cos(O3);
y33=yc-rp*sin(O3);
plot(xc,yc,'*r')
plot(x11,y11,'*r',x21,y21,'*r',x31,y31,'*r')
line([x11,x21],[y11,y21])
line([x21,x31],[y21,y31])
line([x31,x11],[y31,y11])
plot(x12,y12,'*g',x22,y22,'*g',x32,y32,'*g')
plot(x13,y13,'*b',x23,y23,'*b',x33,y33,'*b')
x=[x11,x12,x13];
y=[y11,y12,y13];
x1=[x21,x22,x23];
y1=[y21,y22,y23];
x2=[x31,x32,x33];
y2=[y31,y32,y33];
x3=[x13,x23,x33,x13];
y3=[y13,y23,y33,y13];
plot(x,y,'g')
plot(x1,y1,'g')
plot(x2,y2,'g')
plot(x3,y3,'b')